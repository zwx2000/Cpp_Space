#include <iostream>

using namespace std;

class University
{
public:
    University(string t, string l)
    {
        tower_ = t;
        lake_ = l;
    }
    static string & getLib()
    {
        return lib_;
    }


    void dis()
    {
        cout<<"塔："<<tower_<<" 湖："<<lake_<<" 图："<<lib_<<endl;
    }

private:
    string tower_;
    string lake_;
    static string lib_;
};

string University::lib_ = "";

int main()
{
    University pk("博雅塔","未名湖");
    University bs("电视塔","大明湖");
    University xdf("雷峰塔","西湖");

    University::getLib() += "九阴真经";
    pk.dis();
    University::getLib() += " 中国挖掘机从入门到放弃";
    bs.dis();
    University::getLib() += " 要出国，找我学英语";
    xdf.dis();

    return 0;
}
